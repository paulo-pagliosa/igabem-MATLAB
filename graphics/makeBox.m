function [v, f] = makeBox(p1, p2)
% Makes the vertices and faces of a box
%
% Author: Paulo Pagliosa
% Last revision: 23/10/2024
%
% Input
% =====
% P1 and P2: 1x3 arrays with coordinates of the back bottom left
% and front top right corners of the box
%
% Output
% ======
% V: 8x3 array with the coordinates of the box vertices
% F: 6x4 array with the vertex indices of the box faces
  v = [...
    p1(1), p1(2), p1(3); ...
    p2(1), p1(2), p1(3); ...
    p2(1), p2(2), p1(3); ...
    p1(1), p2(2), p1(3); ...
    p1(1), p1(2), p2(3); ...
    p2(1), p1(2), p2(3); ...
    p2(1), p2(2), p2(3); ...
    p1(1), p2(2), p2(3)];
  f = [...
    1, 5, 8, 4; ...
    2, 6, 7, 3; ...
    1, 5, 6, 2; ...
    4, 3, 7, 8; ...
    1, 2, 3, 4; ...
    5, 8, 7, 6];
end % makeBox
